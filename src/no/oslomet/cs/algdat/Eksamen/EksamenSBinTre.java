    public int antall(T verdi) {
        if(antall()>0 && inneholder(verdi)){
            Node <T> node = rot;
            int teller = 0;
            while(node!=null) {
                if(comp.compare(verdi,node.verdi)==0){
                    teller++;
                    node=node.høyre;
                }
                else if(comp.compare(verdi,node.verdi)>0){
                    node=node.høyre;
                }else{
                    node=node.venstre;
                }
            }

            return teller;
        }else{
            return 0;
        }


    }

